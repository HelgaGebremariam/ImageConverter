// -------------------- Bone color map --------------------
float ColormapBone_red(float x)
{
	if (x < 0.75)
		return 8.0 / 9.0 * x - (13.0 + 8.0 / 9.0) / 1000.0;
	else
		return (13.0 + 8.0 / 9.0) / 10.0 * x - (3.0 + 8.0 / 9.0) / 10.0;
}

float ColormapBone_green(float x)
{
	if (x <= 0.375)
		return 8.0 / 9.0 * x - (13.0 + 8.0 / 9.0) / 1000.0;
	else 
		if (x <= 0.75)
			return (1.0 + 2.0 / 9.0) * x - (13.0 + 8.0 / 9.0) / 100.0;
		else
			return 8.0 / 9.0 * x + 1.0 / 9.0;
}

float ColormapBone_blue(float x)
{
	if (x <= 0.375)
		return (1.0 + 2.0 / 9.0) * x - (13.0 + 8.0 / 9.0) / 1000.0;
	else
		return 8.0 / 9.0 * x + 1.0 / 9.0;
}

vec4 CRichImageColormap::GetColor_Bone(float x)
{
	float r = clamp(ColormapBone_red(x), 0.0, 1.0);
	float g = clamp(ColormapBone_green(x), 0.0, 1.0);
	float b = clamp(ColormapBone_blue(x), 0.0, 1.0);
	return vec4(r, g, b, 1.0);
}


// -------------------- Cool color map --------------------
float ColormapCool_red(float x) 
{
	return (1.0 + 1.0 / 63.0) * x - 1.0 / 63.0;
}

float ColormapCool_green(float x)
{
	return -(1.0 + 1.0 / 63.0) * x + (1.0 + 1.0 / 63.0);
}

vec4 CRichImageColormap::GetColor_Cool(float x)
{
	float r = clamp(ColormapCool_red(x), 0.0, 1.0);
	float g = clamp(ColormapCool_green(x), 0.0, 1.0);
	float b = 1.0;
	return vec4(r, g, b, 1.0);
}


// -------------------- Copper color map --------------------
